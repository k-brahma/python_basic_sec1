"""
数値、文字列の書き方について。
"""

# 数値は生で書きます
print(3)
print(3 + 5)
print(5 * 2)

# 整数だけでなく、小数も扱えます
print(3.5)

# 文字列は、 "ダブルクオート" または 'シングルクオート' で囲みます。
print("こんにちは")
print('こんばんは')

# bool型は、True/Falseのいずれかの値のみ取りえます
print(True)
print(False)

# None型は、「値が指定されていない」ということを示す特別な値
# Excel VBA の Empty に相当
print(None)
