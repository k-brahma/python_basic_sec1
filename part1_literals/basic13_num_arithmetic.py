"""
主な算術演算子(事務、ウェブプログラミングで使うもの限定)

x + y  : 足し算
x - y  : 引き算
x * y  : 掛け算
x / y  : 割り算
x // y : 割り算して、商だけ求める
x % y  : 割り算して、あまりだけ求める
x ** y : x の y 条

x　+=　y: 加算
x　-=　y: 減算
"""

a = 17
b = 5

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

x = 9
y = 5

x += y
print(x)

y = 5
z = 3

y -= z
print(y)

